            obj.javaClass.declaredFields.forEach { field ->
                try {
                    if (field.name != "INSTANCE") {
                        val rField = rClass.getDeclaredField(field.name)
                        rField.isAccessible = true
                        val id = rField.getInt(null)
                        if (id != 0) {
